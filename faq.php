<h1>Perguntas Frequentes</h1>

<h5>1) É possível adicionar outro produto após finalizar a compra?</h5>
<p>Não, para adquirir outro produto, você deverá iniciar uma nova compra.</p>
<h5>2) Como cancelar uma compra?</h5>
<p>Se, dentro dos 7 dias do recebimento do pedido, basta contatar o SAC Nós S. A.: 
    (99) 99999-0001 ou contato@nossa.com. Tenha em mãos o número do pedido e seu CPF.</p>
<h5>3) O que fazer se o produto que eu quiser trocar não estiver mais disponível em estoque?</h5>
<p>Providenciaremos o cancelamento da compra e será gerado um crédito no mesmo valor do produto. 
    É possível o estorno do pagamento se dentro dos sete dias após a compra, conforme 
    descrito em Política de Trocas e Devoluções.</p>
<h5>4) É possível fazer a compra no site do Nós S. A. e buscar os produtos adquiridos na loja física?</h5>
<p>Sim, se esta modalidade estiver disponível no lojista selecionado em seu pedido.</p>
<h5>5) Por que determinado produto não entrega na minha região?</h5>
<p>A decisão das opções de frete é do lojista (retira na loja, frota própria, transportadora, correios). 
    Itens cuja dimensão esteja de acordo com os padrões estabelecidos e o conteúdo seja permitido, é possível
    o envio por Correios para quase todo o Brasil.</p>
<h5>6) O prazo de entrega não foi cumprido. O que faço?</h5>
<p>Situações como esta podem ocorrer, muitas vezes por atrasos nos prestadores do serviço de entrega. 
    Contate o SAC: (99) 99999-0001 ou contato@nossa.com.</p>

